<?php

class Kalkulator {
    
    public function tambah($x, $y)
    {
        return $x + $y;
    }
    public function kurang($x, $y)
    {
        return $x - $y;
    }
    public function kali($x, $y)
    {
        return $x * $y;
    }
    public function bagi($x, $y)
    {
        return $x / $y;
    }

}